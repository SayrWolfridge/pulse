"""
ContextEngine — Pulse v2
=========================
Multi-tier memory architecture for continuous cognitive state.

Tier architecture:
  HOT (48h)      Full fidelity JSONL. Every event. Rolling window, max 10,000 entries.
  WARM (30d)     Compressed day summaries. One entry per day. Generated by ThoughtLoop.
  COLD (all)     Semantic embeddings via FAISS/pgvector. Aged-out hot entries.
  RELATIONSHIP   Per-person JSON files. Never expire.

Day 2 implementation: Hot Tier (full) + stubs for warm/cold/relationship.
Day 3: Warm Tier + Relationship files.
Day 4: Cold Tier (embeddings).

See PULSE_V2_PHASE1_SPRINT.md for full spec.
"""

from __future__ import annotations

import json
import os
import threading
import time
from copy import deepcopy
from datetime import datetime, timezone, timedelta
from pathlib import Path
from typing import Any, Optional

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

HOT_TIER_MAX_ENTRIES = 10_000
HOT_TIER_MAX_AGE_HOURS = 48

# Valid event types logged to hot tier
EVENT_TYPES = {
    "MESSAGE_RECEIVED",
    "MESSAGE_SENT",
    "PULSE_TRIGGER",
    "THOUGHT_LOOP",
    "EMOTIONAL_SHIFT",
    "DRIVE_PEAK",
    "GOAL_PROGRESS",
    "SYSTEM_EVENT",
    "RELATIONSHIP_UPDATE",
    "INSIGHT",
}


def _now_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


def _now_ts() -> float:
    return time.time()


def _ts_from_iso(iso: str) -> float:
    """Convert ISO-8601 string to POSIX timestamp."""
    try:
        dt = datetime.fromisoformat(iso)
        if dt.tzinfo is None:
            dt = dt.replace(tzinfo=timezone.utc)
        return dt.timestamp()
    except (ValueError, TypeError):
        return 0.0


# ---------------------------------------------------------------------------
# HotTier — 48h rolling JSONL window
# ---------------------------------------------------------------------------


class HotTier:
    """
    Append-only JSONL file with a 48h rolling window.

    Thread-safe for concurrent append/read.
    Auto-prunes on load and when entry count exceeds max.
    """

    def __init__(self, filepath: Path):
        self.filepath = filepath
        self._lock = threading.RLock()
        self.filepath.parent.mkdir(parents=True, exist_ok=True)
        # Prune stale entries on startup
        self._prune()

    # ------------------------------------------------------------------
    # Public interface
    # ------------------------------------------------------------------

    def append(self, event: dict) -> None:
        """
        Append one event to the hot tier.

        Required keys: type, content
        Auto-injected: ts (ISO-8601), ts_unix (float)

        Prunes oldest entries if count exceeds HOT_TIER_MAX_ENTRIES.
        """
        with self._lock:
            entry = self._normalize_event(event)
            with open(self.filepath, "a", encoding="utf-8") as f:
                f.write(json.dumps(entry) + "\n")
            # Enforce entry cap lazily (every 100 appends)
            if self._line_count() > HOT_TIER_MAX_ENTRIES:
                self._cap_entries()

    def get_recent(self, hours: int = 2) -> list[dict]:
        """
        Return all events within the last `hours` hours.
        Sorted oldest-first.
        """
        cutoff = _now_ts() - (hours * 3600)
        return self._load(since_ts=cutoff)

    def get_since(self, ts: float) -> list[dict]:
        """Return all events since a given POSIX timestamp."""
        return self._load(since_ts=ts)

    def get_by_type(self, event_type: str, hours: int = 24) -> list[dict]:
        """Filter hot tier by event type within the last N hours."""
        entries = self.get_recent(hours)
        return [e for e in entries if e.get("type") == event_type]

    def get_all(self) -> list[dict]:
        """Return all entries (up to HOT_TIER_MAX_ENTRIES, post-prune)."""
        return self._load(since_ts=None)

    def count(self) -> int:
        """Return current entry count."""
        return self._line_count()

    def clear(self) -> None:
        """Wipe the hot tier. Intended for tests."""
        with self._lock:
            if self.filepath.exists():
                self.filepath.unlink()

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _normalize_event(self, event: dict) -> dict:
        """Ensure all required fields are present."""
        entry = dict(event)
        if "ts" not in entry:
            entry["ts"] = _now_iso()
        if "ts_unix" not in entry:
            entry["ts_unix"] = _ts_from_iso(entry["ts"])
        if "type" not in entry:
            entry["type"] = "SYSTEM_EVENT"
        if "content" not in entry:
            entry["content"] = ""
        if "source" not in entry:
            entry["source"] = "pulse"
        return entry

    def _load(self, since_ts: Optional[float] = None) -> list[dict]:
        """Read JSONL file, return entries optionally filtered by timestamp."""
        entries: list[dict] = []
        if not self.filepath.exists():
            return entries
        with self._lock:
            try:
                with open(self.filepath, "r", encoding="utf-8") as f:
                    for line in f:
                        line = line.strip()
                        if not line:
                            continue
                        try:
                            entry = json.loads(line)
                            if since_ts is not None:
                                entry_ts = entry.get("ts_unix", 0.0)
                                if entry_ts < since_ts:
                                    continue
                            entries.append(entry)
                        except json.JSONDecodeError:
                            continue  # Skip malformed lines
            except OSError:
                return []
        return entries

    def _line_count(self) -> int:
        if not self.filepath.exists():
            return 0
        try:
            with open(self.filepath, "r", encoding="utf-8") as f:
                return sum(1 for line in f if line.strip())
        except OSError:
            return 0

    def _prune(self) -> None:
        """Remove entries older than HOT_TIER_MAX_AGE_HOURS."""
        with self._lock:
            cutoff = _now_ts() - (HOT_TIER_MAX_AGE_HOURS * 3600)
            self._rewrite_filtered(since_ts=cutoff)

    def _cap_entries(self) -> None:
        """Keep only the most recent HOT_TIER_MAX_ENTRIES entries."""
        with self._lock:
            all_entries = self._load(since_ts=None)
            if len(all_entries) <= HOT_TIER_MAX_ENTRIES:
                return
            keep = all_entries[-HOT_TIER_MAX_ENTRIES:]
            self._write_entries(keep)

    def _rewrite_filtered(self, since_ts: float) -> None:
        """Rewrite the file keeping only entries at or after since_ts."""
        entries = self._load(since_ts=since_ts)
        self._write_entries(entries)

    def _write_entries(self, entries: list[dict]) -> None:
        """Atomically rewrite the JSONL file with given entries."""
        tmp = self.filepath.with_suffix(".tmp")
        with open(tmp, "w", encoding="utf-8") as f:
            for entry in entries:
                f.write(json.dumps(entry) + "\n")
        os.replace(tmp, self.filepath)


