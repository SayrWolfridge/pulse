# ColdTier — semantic embeddings (Day 4 stub)
# ---------------------------------------------------------------------------


class ColdTier:
    """
    FAISS semantic index for aged-out hot entries.
    Uses local Ollama nomic-embed-text (768-dim) for embeddings.
    Index + metadata sidecar persisted to disk.
    """

    def __init__(self, index_dir: Path):
        self.index_dir = index_dir
        self.index_dir.mkdir(parents=True, exist_ok=True)
        self._index_path = index_dir / "cold.index"
        self._meta_path = index_dir / "cold-meta.jsonl"
        self._dim = 768  # nomic-embed-text dimension
        self._lock = threading.RLock()
        self._index: Optional[Any] = None  # faiss.IndexFlatIP
        self._metadata: list[dict] = []
        self._load()

    def _load(self) -> None:
        """Load existing index + metadata from disk."""
        with self._lock:
            try:
                import faiss
                if self._index_path.exists():
                    self._index = faiss.read_index(str(self._index_path))
                else:
                    self._index = faiss.IndexFlatIP(self._dim)
            except ImportError:
                # faiss not installed — degrade gracefully
                self._index = None

            self._metadata = []
            if self._meta_path.exists():
                for line in self._meta_path.read_text("utf-8").splitlines():
                    line = line.strip()
                    if line:
                        try:
                            self._metadata.append(json.loads(line))
                        except json.JSONDecodeError:
                            pass

    def _save_index(self) -> None:
        """Persist FAISS index to disk."""
        with self._lock:
            if self._index is not None:
                try:
                    import faiss
                    faiss.write_index(self._index, str(self._index_path))
                except ImportError:
                    pass

    def _embed(self, texts: list[str]) -> list[list[float]]:
        """Call Ollama /api/embeddings for a batch of texts."""
        import http.client as _http
        vectors = []
        conn = _http.HTTPConnection("127.0.0.1", 11434, timeout=30)
        try:
            for text in texts:
                body = json.dumps({"model": "nomic-embed-text", "prompt": text})
                conn.request("POST", "/api/embeddings", body=body,
                             headers={"Content-Type": "application/json"})
                resp = conn.getresponse()
                data = json.loads(resp.read())
                vectors.append(data["embedding"])
        finally:
            conn.close()
        return vectors

    def _normalize(self, vectors: list[list[float]]) -> Any:
        """L2-normalize for cosine similarity via inner product."""
        import numpy as np
        arr = np.array(vectors, dtype=np.float32)
        norms = np.linalg.norm(arr, axis=1, keepdims=True)
        norms[norms == 0] = 1.0
        return arr / norms

    def encode(self, entries: list[dict]) -> int:
        """
        Encode hot-tier entries into the FAISS index.
        Returns the number of entries successfully encoded.
        """
        if not entries or self._index is None:
            return 0
        texts = []
        metas = []
        for e in entries:
            content = str(e.get("content", "")).strip()
            if not content or len(content) < 10:
                continue
            etype = e.get("type", "SYSTEM_EVENT")
            embed_text = f"[{etype}] {content}"
            texts.append(embed_text)
            metas.append({
                "type": etype,
                "content": content[:500],
                "source": e.get("source", ""),
                "ts": e.get("ts", ""),
                "ts_unix": e.get("ts_unix", 0.0),
            })
        if not texts:
            return 0

        try:
            vectors = self._embed(texts)
            normed = self._normalize(vectors)
        except Exception:
            return 0

        with self._lock:
            self._index.add(normed)
            with open(self._meta_path, "a", encoding="utf-8") as f:
                for m in metas:
                    f.write(json.dumps(m) + "\n")
            self._metadata.extend(metas)
            self._save_index()
        return len(texts)

    def search(self, query: str, top_k: int = 5) -> list[dict]:
        """
        Search for semantically similar entries.
        Returns list of dicts with keys: content, type, source, ts, score.
        """
        with self._lock:
            if self._index is None or self._index.ntotal == 0:
                return []

        try:
            query_vec = self._embed([query])
            normed_q = self._normalize(query_vec)
        except Exception:
            return []

        with self._lock:
            k = min(top_k, self._index.ntotal)
            scores, indices = self._index.search(normed_q, k)
            results = []
            for i, idx in enumerate(indices[0]):
                if idx < 0 or idx >= len(self._metadata):
                    continue
                meta = dict(self._metadata[idx])
                meta["score"] = float(scores[0][i])
                results.append(meta)
        return results

    def count(self) -> int:
        with self._lock:
            return self._index.ntotal if self._index else 0


# ---------------------------------------------------------------------------
# ContextEngine — top-level coordinator
# ---------------------------------------------------------------------------


class ContextEngine:
    """
    Multi-tier memory engine for Pulse v2.

    Tiers:
      hot          — last 48h, full fidelity JSONL (LIVE)
      warm         — day summaries, 30d retention (Day 3)
      cold         — semantic index, unbounded (Day 4)
      relationship — per-person persistent files (Day 3)

    Usage:
        engine = ContextEngine(Path("~/.pulse/state").expanduser())
        engine.log_event({"type": "MESSAGE_RECEIVED", "content": "...", "source": "Josh"})
        recent = engine.get_recent_context(hours=2)
        ctx = engine.get_project_context("weather-bot")
    """

    def __init__(self, state_dir: Optional[Path] = None):
        if state_dir is None:
            state_dir = Path("~/.pulse/state").expanduser()
        self.state_dir = Path(state_dir)
        self.state_dir.mkdir(parents=True, exist_ok=True)

        self.hot = HotTier(self.state_dir / "context-hot.jsonl")
        self.warm = WarmTier(self.state_dir / "context-warm.json")
        self.cold = ColdTier(self.state_dir / "cold-tier")
        self.relationship = RelationshipTier(self.state_dir / "relationships")

    # ------------------------------------------------------------------
    # Core event logging
    # ------------------------------------------------------------------

    def log_event(self, event: dict) -> None:
        """
        Append an event to the hot tier.

        Minimum required: {type, content}
        Optional: source, metadata (any extra keys)

        Example:
            engine.log_event({
                "type": "MESSAGE_RECEIVED",
                "content": "Hey Iris, how's the weather bot doing?",
                "source": "Josh",
            })
        """
        if event.get("type") not in EVENT_TYPES:
            event = dict(event)
            event["type"] = "SYSTEM_EVENT"
        self.hot.append(event)

    def log_pulse_trigger(self, drive: str, pressure: float, summary: str) -> None:
        """Convenience: log a Pulse trigger event."""
        self.log_event(
            {
                "type": "PULSE_TRIGGER",
                "content": summary,
                "source": "pulse",
                "drive": drive,
                "pressure": pressure,
            }
        )

    def log_message(
        self, direction: str, content: str, source: str = "unknown"
    ) -> None:
        """
        Convenience: log an incoming or outgoing message.
        direction: "received" | "sent"
        """
        event_type = (
            "MESSAGE_RECEIVED" if direction == "received" else "MESSAGE_SENT"
        )
        self.log_event({"type": event_type, "content": content, "source": source})

    def log_insight(self, content: str, source: str = "thought_loop") -> None:
        """Log a generated insight."""
        self.log_event({"type": "INSIGHT", "content": content, "source": source})

    def log_emotional_shift(
        self, from_state: str, to_state: str, trigger: str = ""
    ) -> None:
        """Log a significant emotional state change."""
        self.log_event(
            {
                "type": "EMOTIONAL_SHIFT",
                "content": f"{from_state} → {to_state}",
                "source": "emotional_engine",
                "from_state": from_state,
                "to_state": to_state,
                "trigger": trigger,
            }
        )

    # ------------------------------------------------------------------
    # Retrieval
    # ------------------------------------------------------------------

    def get_recent_context(self, hours: int = 2) -> list[dict]:
        """
        Pull from hot tier. Used for ThoughtLoop continuity.
        Returns events sorted oldest-first.
        """
        return self.hot.get_recent(hours=hours)

    def get_project_context(self, project: str) -> dict:
        """
        Cross-tier context for a project.
        Hot for recent activity, warm for arc, cold for history.
        """
        recent = [
            e
            for e in self.hot.get_recent(hours=72)
            if project.lower() in str(e.get("content", "")).lower()
            or project.lower() in str(e.get("metadata", "")).lower()
        ]
        warm_recent = self.warm.get_recent_days(7)
        return {
            "project": project,
            "recent_events": recent[-20:],  # Last 20 matching hot entries
            "warm_arc": warm_recent,
            "cold_hits": self.cold.search(project, top_k=5),
        }

    def get_events_by_type(self, event_type: str, hours: int = 24) -> list[dict]:
        """Filter hot tier by event type."""
        return self.hot.get_by_type(event_type, hours=hours)

    # ------------------------------------------------------------------
    # Relationship management
    # ------------------------------------------------------------------

    def get_relationship(self, person: str) -> dict:
        """Always-fresh relationship context for a person."""
        return self.relationship.get(person)

    def update_relationship(self, person: str, event: dict) -> None:
        """Called after every meaningful interaction with a person."""
        self.relationship.update(person, event)

        # Also log to hot tier (unless suppressed)
        if event.get("_log", True) is False:
            return

        self.log_event(
            {
                "type": "RELATIONSHIP_UPDATE",
                "content": f"Interaction with {person}",
                "source": "context_engine",
                "person": person,
                **{k: v for k, v in event.items() if k != "note" and not str(k).startswith("_")},
            }
        )

    def get_all_relationships(self) -> dict[str, dict]:
        """Return all relationship data."""
        return self.relationship.get_all()

    # ------------------------------------------------------------------
    # Compression (Day 3 — warm tier generation)
    # ------------------------------------------------------------------

    def compress_to_warm(self, date: Optional[str] = None) -> dict:
        """
        Summarize a day's hot entries into the warm tier.
        Called by ThoughtLoop. Full local-model synthesis in Day 3.
        """
        if date is None:
            date = datetime.now(timezone.utc).strftime("%Y-%m-%d")
        # Get entries for that date
        start_of_day = datetime.strptime(date, "%Y-%m-%d").replace(
            tzinfo=timezone.utc
        )
        end_of_day = start_of_day + timedelta(days=1)
        all_entries = self.hot.get_all()
        day_entries = [
            e
            for e in all_entries
            if start_of_day.timestamp()
            <= e.get("ts_unix", 0.0)
            < end_of_day.timestamp()
        ]
        summary = self.warm.compress_day(date, day_entries)
        self.warm.write_day(date, summary)
        return summary

    def encode_to_cold(self, entries: list[dict]) -> int:
        """Move aged-out hot entries into semantic embedding store."""
        return self.cold.encode(entries)

    # ------------------------------------------------------------------
    # Status
    # ------------------------------------------------------------------

    def status(self) -> dict:
        """Return quick status snapshot."""
        return {
            "hot_entries": self.hot.count(),
            "hot_max_age_hours": HOT_TIER_MAX_AGE_HOURS,
            "hot_max_entries": HOT_TIER_MAX_ENTRIES,
            "warm_days": len(self.warm.get_recent_days(30)),
            "cold_entries": self.cold.count(),
            "known_people": self.relationship.list_people(),
        }
