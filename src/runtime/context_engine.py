class WarmTier:
    """
    One summary per day. Generated by ThoughtLoop during off-peak hours.
    Day 3 implementation — full template-based compression.
    Local-model synthesis layer planned for Day 4 ThoughtLoop integration.
    """

    def __init__(self, filepath: Path):
        self.filepath = filepath
        self.filepath.parent.mkdir(parents=True, exist_ok=True)

    def get_day(self, date: str) -> Optional[dict]:
        """Retrieve summary for a specific date (YYYY-MM-DD). Returns None if absent."""
        if not self.filepath.exists():
            return None
        try:
            with open(self.filepath, "r", encoding="utf-8") as f:
                data = json.load(f)
            return data.get(date)
        except (OSError, json.JSONDecodeError):
            return None

    def get_recent_days(self, n: int = 7) -> list[dict]:
        """Return up to N most recent day summaries."""
        if not self.filepath.exists():
            return []
        try:
            with open(self.filepath, "r", encoding="utf-8") as f:
                data = json.load(f)
            sorted_days = sorted(data.items(), key=lambda x: x[0], reverse=True)
            return [v for _, v in sorted_days[:n]]
        except (OSError, json.JSONDecodeError):
            return []

    def write_day(self, date: str, summary: dict) -> None:
        """Write or overwrite a day summary. Called by ThoughtLoop."""
        data: dict = {}
        if self.filepath.exists():
            try:
                with open(self.filepath, "r", encoding="utf-8") as f:
                    data = json.load(f)
            except (OSError, json.JSONDecodeError):
                data = {}
        data[date] = summary
        tmp = self.filepath.with_suffix(".tmp")
        with open(tmp, "w", encoding="utf-8") as f:
            json.dump(data, f, indent=2)
        os.replace(tmp, self.filepath)

    def delete_day(self, date: str) -> bool:
        """Remove a day summary. Returns True if it existed."""
        if not self.filepath.exists():
            return False
        try:
            with open(self.filepath, "r", encoding="utf-8") as f:
                data = json.load(f)
        except (OSError, json.JSONDecodeError):
            return False
        if date not in data:
            return False
        del data[date]
        tmp = self.filepath.with_suffix(".tmp")
        with open(tmp, "w", encoding="utf-8") as f:
            json.dump(data, f, indent=2)
        os.replace(tmp, self.filepath)
        return True

    def list_dates(self) -> list[str]:
        """Return all dates with warm summaries, sorted newest-first."""
        if not self.filepath.exists():
            return []
        try:
            with open(self.filepath, "r", encoding="utf-8") as f:
                data = json.load(f)
            return sorted(data.keys(), reverse=True)
        except (OSError, json.JSONDecodeError):
            return []

    def compress_day(self, date: str, hot_entries: list[dict]) -> dict:
        """
        Build a warm summary from a list of hot entries.
        Day 3: full template-based extraction (no LLM required).
        Local-model synthesis will layer on top in Day 4 (ThoughtLoop).

        Extracted fields:
          themes          — unique event types present
          emotional_arc   — derived from EMOTIONAL_SHIFT events
          key_events      — top events by priority (non-system first)
          projects_touched — extracted from PULSE_TRIGGER content
          mood_avg        — derived from endocrine/drive data where available
          drive_peaks     — drives that fired during the day
          message_count   — total MESSAGE_RECEIVED + MESSAGE_SENT
          pulse_triggers  — count of PULSE_TRIGGER events
        """
        if not hot_entries:
            return {
                "date": date,
                "event_count": 0,
                "themes": [],
                "emotional_arc": "quiet",
                "key_events": [],
                "projects_touched": [],
                "mood_avg": 0.5,
                "drive_peaks": [],
                "message_count": 0,
                "pulse_triggers": 0,
                "generated_at": _now_iso(),
                "generated_by": "template_v1",
            }

        # --- Themes: unique event types ---
        themes: list[str] = sorted(
            {e.get("type", "SYSTEM_EVENT") for e in hot_entries}
        )

        # --- Emotional arc: derived from EMOTIONAL_SHIFT transitions ---
        shifts = [
            e for e in hot_entries if e.get("type") == "EMOTIONAL_SHIFT"
        ]
        if shifts:
            # Arc: first_state → last_state (compressed)
            first = shifts[0].get("from_state", "")
            last = shifts[-1].get("to_state", "")
            if first and last and first != last:
                emotional_arc = f"{first} → {last}"
            elif last:
                emotional_arc = last
            elif first:
                emotional_arc = first
            else:
                emotional_arc = "shifting"
        elif any(e.get("type") == "EMOTIONAL_SHIFT" for e in hot_entries):
            emotional_arc = "active"
        else:
            # Infer from event mix
            type_counts: dict[str, int] = {}
            for e in hot_entries:
                t = e.get("type", "")
                type_counts[t] = type_counts.get(t, 0) + 1
            if type_counts.get("PULSE_TRIGGER", 0) > 5:
                emotional_arc = "driven"
            elif type_counts.get("MESSAGE_RECEIVED", 0) > 3:
                emotional_arc = "connected"
            elif type_counts.get("GOAL_PROGRESS", 0) > 0:
                emotional_arc = "progressing"
            elif type_counts.get("INSIGHT", 0) > 0:
                emotional_arc = "reflective"
            else:
                emotional_arc = "neutral"

        # --- Key events: top 5, non-system preferred ---
        priority_types = {
            "GOAL_PROGRESS": 5,
            "INSIGHT": 4,
            "EMOTIONAL_SHIFT": 3,
            "PULSE_TRIGGER": 2,
            "MESSAGE_RECEIVED": 2,
            "MESSAGE_SENT": 2,
            "RELATIONSHIP_UPDATE": 3,
            "THOUGHT_LOOP": 1,
            "DRIVE_PEAK": 1,
            "SYSTEM_EVENT": 0,
        }
        scored_entries = sorted(
            hot_entries,
            key=lambda e: (
                priority_types.get(e.get("type", ""), 0),
                e.get("ts_unix", 0.0),
            ),
            reverse=True,
        )
        key_events: list[str] = []
        seen_snippets: set[str] = set()
        for e in scored_entries:
            content = str(e.get("content", "")).strip()
            if not content:
                continue
            snippet = content[:140]
            if snippet in seen_snippets:
                continue
            seen_snippets.add(snippet)
            key_events.append(snippet)
            if len(key_events) >= 5:
                break

        # --- Projects touched: extracted from PULSE_TRIGGER content ---
        project_keywords = [
            "anima", "gnosis", "weather", "sdca", "pulse", "invoiceflow",
            "cpi", "hypostas", "locus", "aether", "bios", "3d internet",
            "local-model", "constellation", "context_engine", "state_engine",
            "thought_loop", "runtime_bridge",
        ]
        projects_found: set[str] = set()
        for e in hot_entries:
            content_lower = str(e.get("content", "")).lower()
            for proj in project_keywords:
                if proj in content_lower:
                    projects_found.add(proj)
        projects_touched = sorted(projects_found)

        # --- Drive peaks: drives that fired (from DRIVE_PEAK events) ---
        drive_peaks: list[str] = []
        for e in hot_entries:
            if e.get("type") == "DRIVE_PEAK":
                drive = e.get("drive", e.get("content", ""))
                if drive and drive not in drive_peaks:
                    drive_peaks.append(drive)
        # Also extract from PULSE_TRIGGER drive field
        for e in hot_entries:
            if e.get("type") == "PULSE_TRIGGER":
                drive = e.get("drive", "")
                if drive and drive not in drive_peaks:
                    drive_peaks.append(drive)

        # --- Mood average: derived from endocrine fields where available ---
        valence_samples: list[float] = []
        for e in hot_entries:
            # Look for valence in event metadata
            valence = e.get("valence")
            if valence is not None:
                try:
                    valence_samples.append(float(valence))
                except (TypeError, ValueError):
                    pass
            # Infer from endocrine if present
            endocrine = e.get("endocrine", {})
            if endocrine:
                dopamine = endocrine.get("dopamine", 0.0)
                oxytocin = endocrine.get("oxytocin", 0.0)
                cortisol = endocrine.get("cortisol", 0.0)
                if dopamine or oxytocin or cortisol:
                    # Simple positive bias formula: (dopa + oxt - cortisol/2) / 20
                    sample = (
                        (dopamine + oxytocin - cortisol * 0.5) / 20.0
                    )
                    valence_samples.append(max(0.0, min(1.0, sample)))
        mood_avg = (
            round(sum(valence_samples) / len(valence_samples), 3)
            if valence_samples
            else 0.5
        )

        # --- Message and trigger counts ---
        message_count = sum(
            1
            for e in hot_entries
            if e.get("type") in ("MESSAGE_RECEIVED", "MESSAGE_SENT")
        )
        pulse_trigger_count = sum(
            1 for e in hot_entries if e.get("type") == "PULSE_TRIGGER"
        )

        return {
            "date": date,
            "event_count": len(hot_entries),
            "themes": themes,
            "emotional_arc": emotional_arc,
            "key_events": key_events,
            "projects_touched": projects_touched,
            "mood_avg": mood_avg,
            "drive_peaks": drive_peaks,
            "message_count": message_count,
            "pulse_triggers": pulse_trigger_count,
            "generated_at": _now_iso(),
            "generated_by": "template_v1",
        }


