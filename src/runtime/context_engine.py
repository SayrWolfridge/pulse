# RelationshipTier — per-person JSON files (Day 3 implementation)
# ---------------------------------------------------------------------------


class RelationshipTier:
    """
    Per-person JSON files in `relationships/` subdirectory.
    Never expire. Track: last seen, conversation threads, bond strength.
    Day 3 full implementation — basic CRUD here.
    """

    def __init__(self, directory: Path):
        self.directory = directory
        self.directory.mkdir(parents=True, exist_ok=True)

    def _person_file(self, person: str) -> Path:
        safe = person.lower().replace(" ", "_").replace("/", "_")
        return self.directory / f"{safe}.json"

    def get(self, person: str) -> dict:
        """Return relationship data for a person. Empty dict if unknown."""
        path = self._person_file(person)
        if not path.exists():
            return {}
        try:
            with open(path, "r", encoding="utf-8") as f:
                return json.load(f)
        except (OSError, json.JSONDecodeError):
            return {}

    def update(self, person: str, event: dict) -> None:
        """
        Merge an event into a person's relationship file.
        Creates the file if it doesn't exist.
        """
        touch = event.get("_touch", True) is not False

        data = self.get(person)
        if not data:
            # Allow callers to seed first_seen/last_seen on first creation.
            seeded_first = event.get("first_seen") or _now_iso()
            seeded_last = event.get("last_seen") or _now_iso()
            data = {
                "person": person,
                "first_seen": seeded_first,
                "last_seen": seeded_last,
                "interaction_count": 0,
                "bond_strength": 0.5,
                "notes": [],
                "recent_themes": [],
            }

        if touch:
            data["last_seen"] = _now_iso()
            data["interaction_count"] = data.get("interaction_count", 0) + 1
        else:
            # Non-touch update (e.g. decay sweep). Preserve last_seen + interaction_count.
            if "last_seen" in event:
                data["last_seen"] = event["last_seen"]
            if "first_seen" in event and "first_seen" not in data:
                data["first_seen"] = event["first_seen"]
        # Append note if provided
        note = event.get("note")
        if note:
            notes: list = data.get("notes", [])
            notes.append({"ts": _now_iso(), "content": note})
            # Keep last 50 notes
            data["notes"] = notes[-50:]
        # Merge extra fields from event
        # NOTE: Some fields are intentionally mutable (bond strength, themes, threads).
        mutable_fields = {
            "bond_strength",
            "recent_themes",
            "active_threads",
            "current_context",
            "what_he_needs_right_now",
            "tier",
            "kind",
        }
        for k, v in event.items():
            if k in ("note", "ts", "ts_unix", "first_seen", "last_seen"):
                continue
            # Internal control keys (do not persist)
            if k.startswith("_"):
                continue
            if k in mutable_fields:
                # Special merge for recent_themes
                if k == "recent_themes" and isinstance(v, list):
                    existing = list(data.get("recent_themes") or [])
                    for t in v:
                        t = str(t).strip()
                        if not t:
                            continue
                        if t in existing:
                            existing.remove(t)
                        existing.append(t)
                    data["recent_themes"] = existing[-20:]
                else:
                    data[k] = v
            else:
                # Non-mutable fields: only set if missing (preserve history)
                if k not in data:
                    data[k] = v
        path = self._person_file(person)
        tmp = path.with_suffix(".tmp")
        with open(tmp, "w", encoding="utf-8") as f:
            json.dump(data, f, indent=2)
        os.replace(tmp, path)

    def get_all(self) -> dict[str, dict]:
        """Return all relationship files as {person: data}."""
        result: dict[str, dict] = {}
        for path in sorted(self.directory.glob("*.json")):
            try:
                with open(path, "r", encoding="utf-8") as f:
                    data = json.load(f)
                result[data.get("person", path.stem)] = data
            except (OSError, json.JSONDecodeError):
                continue
        return result

    def list_people(self) -> list[str]:
        """Return list of known person names."""
        result = []
        for path in sorted(self.directory.glob("*.json")):
            try:
                with open(path, "r", encoding="utf-8") as f:
                    data = json.load(f)
                result.append(data.get("person", path.stem))
            except (OSError, json.JSONDecodeError):
                result.append(path.stem.replace("_", " "))
        return result

    def seed(self, person: str, seed_data: dict, overwrite: bool = False) -> bool:
        """
        Seed a relationship file with pre-populated data.
        Used at startup to pre-load known context from SOUL.md / USER.md.

        Returns True if file was written, False if it already existed and
        overwrite=False.
        """
        path = self._person_file(person)
        if path.exists() and not overwrite:
            return False
        data = {
            "person": person,
            "first_seen": seed_data.get("first_seen", _now_iso()),
            "last_seen": seed_data.get("last_seen", _now_iso()),
            "interaction_count": seed_data.get("interaction_count", 0),
            "bond_strength": seed_data.get("bond_strength", 0.5),
            "notes": seed_data.get("notes", []),
            "recent_themes": seed_data.get("recent_themes", []),
            "seeded_at": _now_iso(),
            "seeded_from": seed_data.get("seeded_from", "manual"),
            **{
                k: v
                for k, v in seed_data.items()
                if k
                not in (
                    "person",
                    "first_seen",
                    "last_seen",
                    "interaction_count",
                    "bond_strength",
                    "notes",
                    "recent_themes",
                    "seeded_at",
                    "seeded_from",
                )
            },
        }
        tmp = path.with_suffix(".tmp")
        with open(tmp, "w", encoding="utf-8") as f:
            json.dump(data, f, indent=2)
        os.replace(tmp, path)
        return True

    def delete(self, person: str) -> bool:
        """Remove a relationship file. Returns True if it existed."""
        path = self._person_file(person)
        if not path.exists():
            return False
        path.unlink()
        return True


